(function(){
const html = document.documentElement;
const select = document.getElementById('lang');
const saved = localStorage.getItem('lang') || (navigator.language||'fr').slice(0,2);
const initial = ['fr','en','es'].includes(saved) ? saved : 'fr';
if (select) select.value = initial;
applyLang(initial);

if (select) {
select.addEventListener('change', () => applyLang(select.value));
}

function t(key){
const lang = html.dataset.lang || 'fr';
return I18N[lang]?.[key] ?? I18N.fr[key] ?? key;
}

function applyLang(lang){
html.lang = lang;
html.dataset.lang = lang;
localStorage.setItem('lang', lang);


document.querySelectorAll('[data-i18n]').forEach(el=>{
  const key = el.getAttribute('data-i18n');
  el.textContent = t(key);
});

document.querySelectorAll('[data-i18n-attr]').forEach(el=>{
  const attrs = el.getAttribute('data-i18n-attr').split(',').map(s=>s.trim());
  attrs.forEach(attr=>{
    const key = el.getAttribute('data-i18n') || el.getAttribute('data-i18n-key');
    if (key) el.setAttribute(attr, t(key));
  });
});

const pickup = document.getElementById('pickup');
const dropoff = document.getElementById('dropoff');
const notes = document.getElementById('notes');
if(pickup) pickup.placeholder = t('booking.placeholder.pickup');
if(dropoff) dropoff.placeholder = t('booking.placeholder.dropoff');
if(notes) notes.placeholder = t('booking.placeholder.notes');

const titleEl = document.querySelector('title[data-i18n="meta.title"]');
if(titleEl) titleEl.textContent = t('meta.title');
const metaDesc = document.querySelector('meta[name="description"]');
if(metaDesc) metaDesc.setAttribute('content', t('meta.title'));
}

window.__t = (key) => t(key);
})();